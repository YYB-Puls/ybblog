package Day08.ClassAndObject;

public class PrintCarStatus {
    public static void main(String[] args) {
        int speed;
        String color;
        String name;
        String direaction;

        speed = 60;
        color = "白色";
        name = "雪铁龙一号";
        direaction = "镇江方向";

        System.out.println("经过的汽车速度为:"+speed);
        System.out.println("它的颜色是:"+color);
        System.out.println("汽车的名称是:"+name);
        System.out.println("行驶的方向是:"+direaction);
    }
}
